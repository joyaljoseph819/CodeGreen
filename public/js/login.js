$("#login-div").mouseenter(function(){
    $("#login_tt").tooltip('show');
	setTimeout(function(){$("#login_tt").tooltip('hide')},2000);
  });
$("#login-div").click(function(){    
	setTimeout(function(){$("#center_tt").removeClass('tt_show')},1000);
  });
$("#register").mouseenter(function(){
    $(".register_tt").tooltip('show');
	setTimeout(function(){$(".register_tt").tooltip('hide')},2000);
  });
$("#clickhide").click(function(){
    $(".register_tt").tooltip('hide');
});
$("#ahaabtn").click(function(){
    $("#register").removeClass('active');
    $("#login-div").addClass('active');
});
$("#caabtn").click(function(){
    $("#login-div").removeClass('active');
    $("#register").addClass('active');
});
